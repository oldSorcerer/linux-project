#!/bin/bash

# Используем команду `lsblk` для отображения только нужных столбцов:
# имя устройства (NAME), размер (SIZE), тип файловой системы (FSTYPE), точка монтирования (MOUNTPOINT)
???

# Находим устройство с файловой системой типа `ext4` и берём его точку монтирования
MOUNT_POINT=$(??? | awk '$1 == "ext4" {print $2}')

# Если точка монтирования найдена, выводим её
if [ -n "$MOUNT_POINT" ]; then
    echo "Точка монтирования устройства с файловой системой ext4: $MOUNT_POINT"
else
    echo "Устройство с файловой системой ext4 не найдено."
fi