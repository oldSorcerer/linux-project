# Чтение файла salaries.txt и добавление 10% к значению третьего столбца,
# Вывод результата с форматированием и выравниванием

awk '{
    # Получение значений столбцов
    name = ???
    age = ???
    salary = ???

    # Расчёт новой зарплаты (увеличение на 10%)
    new_salary = salary * 1.10

    # Форматированный вывод с двумя знаками после запятой и выравниванием
    printf "%-10s %-5d %-10.2f\n", name, age, new_salary
}' salaries.txt